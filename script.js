  reactionGame.lastCurrent = current;
  reactionGame.currentNode.textContent = current === null ? "-- ms" : `${Math.round(current)} ms`;
  reactionGame.bestNode.textContent = reactionGame.best === null ? "-- ms" : `${Math.round(reactionGame.best)} ms`;
