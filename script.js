function renderMemoryBoard() {
  memoryGame.boardNode.innerHTML = "";
  memoryGame.cards.forEach((card, index) => {
    const isFlipped = memoryGame.flipped.includes(index) || card.matched;
    const cardButton = document.createElement("button");
    cardButton.className = `memory-card${isFlipped ? "" : " hidden"}${card.matched ? " matched" : ""}`;
    cardButton.textContent = isFlipped ? card.symbol : "✦";
    cardButton.addEventListener("click", () => flipMemoryCard(index));
    memoryGame.boardNode.appendChild(cardButton);
  });
  renderMemoryHUD();
}

function resetMemoryGame() {
  const deck = shuffleArray([...memoryGame.symbols, ...memoryGame.symbols]).map((symbol) => ({
    symbol,
    matched: false,
  }));
  memoryGame.cards = deck;
  memoryGame.flipped = [];
  memoryGame.moves = 0;
  memoryGame.matchedPairs = 0;
  memoryGame.lock = false;
  setMemoryMessage("msgMemoryStart");
  renderMemoryBoard();
}

function flipMemoryCard(index) {
  if (memoryGame.lock || memoryGame.flipped.includes(index) || memoryGame.cards[index].matched) {
    return;
  }

  memoryGame.flipped.push(index);
  renderMemoryBoard();

  if (memoryGame.flipped.length < 2) {
    setMemoryMessage("msgMemoryNext");
    return;
  }

  memoryGame.moves += 1;
  const [firstIndex, secondIndex] = memoryGame.flipped;
  const firstCard = memoryGame.cards[firstIndex];
  const secondCard = memoryGame.cards[secondIndex];

  if (firstCard.symbol === secondCard.symbol) {
    firstCard.matched = true;
    secondCard.matched = true;
    memoryGame.flipped = [];
    memoryGame.matchedPairs += 1;
    if (memoryGame.matchedPairs === memoryGame.symbols.length) {
      setMemoryMessage("msgMemoryDone", { moves: memoryGame.moves });
    } else {
      setMemoryMessage("msgMemoryMatch");
    }
    renderMemoryBoard();
    return;
  }

  memoryGame.lock = true;
  setMemoryMessage("msgMemoryMiss");
  window.setTimeout(() => {
    memoryGame.flipped = [];
    memoryGame.lock = false;
    renderMemoryBoard();
  }, 700);
}

memoryGame.resetButton.addEventListener("click", resetMemoryGame);

