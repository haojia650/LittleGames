function addRandom2048Tile() {
  const emptyTiles = [];
  game2048.grid.forEach((row, rowIndex) => {
    row.forEach((cell, colIndex) => {
      if (cell === 0) {
        emptyTiles.push([rowIndex, colIndex]);
      }
    });
  });
  if (!emptyTiles.length) {
    return;
  }
  const [row, col] = emptyTiles[Math.floor(Math.random() * emptyTiles.length)];
  game2048.grid[row][col] = Math.random() < 0.9 ? 2 : 4;
}

function reset2048() {
  game2048.grid = create2048Matrix();
  game2048.score = 0;
  game2048.won = false;
  game2048.over = false;
  addRandom2048Tile();
  addRandom2048Tile();
  set2048Message("msg2048Start");
  render2048();
}

function slide2048Line(line) {
  const original = [...line];
  const compact = line.filter(Boolean);
  const merged = [];
  let gained = 0;

  for (let i = 0; i < compact.length; i += 1) {
    if (compact[i] === compact[i + 1]) {
      const nextValue = compact[i] * 2;
      merged.push(nextValue);
      gained += nextValue;
      i += 1;
    } else {
      merged.push(compact[i]);
    }
  }

  while (merged.length < game2048.size) {
    merged.push(0);
  }

  return {
    line: merged,
    gained,
    moved: merged.some((value, index) => value !== original[index]),
  };
}

function canMove2048() {
  for (let row = 0; row < game2048.size; row += 1) {
    for (let col = 0; col < game2048.size; col += 1) {
      const current = game2048.grid[row][col];
      if (current === 0) {
        return true;
      }
      if (row < game2048.size - 1 && current === game2048.grid[row + 1][col]) {
        return true;
      }
      if (col < game2048.size - 1 && current === game2048.grid[row][col + 1]) {
        return true;
      }
    }
  }
  return false;
}

function move2048(direction) {
  if (game2048.over) {
    set2048Message("msg2048Locked");
    return;
  }

  let moved = false;
  let gainedTotal = 0;

  if (direction === "left" || direction === "right") {
    game2048.grid = game2048.grid.map((row) => {
      const line = direction === "right" ? [...row].reverse() : [...row];
      const result = slide2048Line(line);
      if (result.moved) {
        moved = true;
      }
      gainedTotal += result.gained;
      return direction === "right" ? result.line.reverse() : result.line;
    });
  } else {
    for (let col = 0; col < game2048.size; col += 1) {
      const column = [];
      for (let row = 0; row < game2048.size; row += 1) {
        column.push(game2048.grid[row][col]);
      }
      const line = direction === "down" ? column.reverse() : column;
      const result = slide2048Line(line);
      const finalColumn = direction === "down" ? result.line.reverse() : result.line;
      if (result.moved) {
        moved = true;
      }
      gainedTotal += result.gained;
      for (let row = 0; row < game2048.size; row += 1) {
        game2048.grid[row][col] = finalColumn[row];
      }
    }
  }

  if (!moved) {
    return;
  }

  game2048.score += gainedTotal;
  addRandom2048Tile();

  if (!game2048.won && game2048.grid.flat().some((value) => value >= 2048)) {
    game2048.won = true;
    set2048Message("msg2048Won");
  } else if (!canMove2048()) {
    game2048.over = true;
    set2048Message("msg2048Over");
  } else {
    set2048Message("msg2048Continue");
  }

  render2048();
}

