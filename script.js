function getTttWinner(board) {
  const lines = [
    [0, 1, 2],
    [3, 4, 5],
    [6, 7, 8],
    [0, 3, 6],
    [1, 4, 7],
    [2, 5, 8],
    [0, 4, 8],
    [2, 4, 6],
  ];
  for (const [a, b, c] of lines) {
    if (board[a] && board[a] === board[b] && board[a] === board[c]) {
      return board[a];
    }
  }
  return board.every(Boolean) ? "draw" : null;
}

function resetTttGame() {
  tttGame.board = Array(9).fill("");
  tttGame.turn = tttGame.player;
  tttGame.over = false;
  setTttMessage("msgTttTurn");
  renderTtt();
}

function aiMoveTtt() {
  if (tttGame.over) {
    return;
  }
  const empties = tttGame.board.map((cell, index) => (cell ? null : index)).filter((value) => value !== null);
  if (!empties.length) {
    return;
  }
  const index = empties[Math.floor(Math.random() * empties.length)];
  tttGame.board[index] = tttGame.ai;
  const result = getTttWinner(tttGame.board);
  if (result === tttGame.ai) {
    tttGame.over = true;
    tttGame.score.ai += 1;
    setTttMessage("msgTttAiWin");
  } else if (result === "draw") {
    tttGame.over = true;
    setTttMessage("msgTttDraw");
  } else {
    tttGame.turn = tttGame.player;
    setTttMessage("msgTttTurn");
  }
  renderTtt();
}

function playTtt(index) {
  if (tttGame.over || tttGame.turn !== tttGame.player || tttGame.board[index]) {
    return;
  }
  tttGame.board[index] = tttGame.player;
  const result = getTttWinner(tttGame.board);
  if (result === tttGame.player) {
    tttGame.over = true;
    tttGame.score.player += 1;
    setTttMessage("msgTttYouWin");
    renderTtt();
    return;
  }
  if (result === "draw") {
    tttGame.over = true;
    setTttMessage("msgTttDraw");
    renderTtt();
    return;
  }
  tttGame.turn = tttGame.ai;
  setTttMessage("msgTttAiThinking");
  renderTtt();
  window.setTimeout(aiMoveTtt, 320);
}

