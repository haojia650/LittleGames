function createPopBoard() {
  return Array.from({ length: popGame.rows }, () =>
    Array.from({ length: popGame.cols }, () => popGame.colors[Math.floor(Math.random() * popGame.colors.length)]),
  );
}

function getPopGroup(startRow, startCol) {
  const target = popGame.board[startRow][startCol];
  if (!target) {
    return [];
  }
  const visited = new Set();
  const stack = [[startRow, startCol]];
  const group = [];
  while (stack.length) {
    const [row, col] = stack.pop();
    const key = `${row},${col}`;
    if (visited.has(key)) {
      continue;
    }
    visited.add(key);
    if (row < 0 || col < 0 || row >= popGame.rows || col >= popGame.cols) {
      continue;
    }
    if (popGame.board[row][col] !== target) {
      continue;
    }
    group.push([row, col]);
    stack.push([row - 1, col], [row + 1, col], [row, col - 1], [row, col + 1]);
  }
  return group;
}

function collapsePopBoard() {
  for (let col = 0; col < popGame.cols; col += 1) {
    const values = [];
    for (let row = popGame.rows - 1; row >= 0; row -= 1) {
      if (popGame.board[row][col]) {
        values.push(popGame.board[row][col]);
      }
    }
    for (let row = popGame.rows - 1, index = 0; row >= 0; row -= 1, index += 1) {
      popGame.board[row][col] = values[index] || null;
    }
  }
}

function renderPopBoard() {
  popGame.boardNode.innerHTML = "";
  popGame.board.flat().forEach((cell, index) => {
    const button = document.createElement("button");
    button.className = `pop-cell${cell ? "" : " empty"}`;
    button.style.background = cell || "rgba(255,255,255,0.04)";
    const row = Math.floor(index / popGame.cols);
    const col = index % popGame.cols;
    button.addEventListener("click", () => playPop(row, col));
    popGame.boardNode.appendChild(button);
  });
  renderPopHud();
}

function resetPopGame() {
  popGame.board = createPopBoard();
  popGame.score = 0;
  setPopMessage("msgPopStart");
  renderPopBoard();
}

function playPop(row, col) {
  if (!popGame.board[row][col]) {
    return;
  }
  const group = getPopGroup(row, col);
  if (group.length < 2) {
    setPopMessage("msgPopTooSmall");
    return;
  }
  group.forEach(([r, c]) => {
    popGame.board[r][c] = null;
  });
  collapsePopBoard();
  popGame.score += group.length * group.length;
  setPopMessage("msgPopClear", { count: group.length });
  renderPopBoard();
  if (popGame.board.flat().every((cell) => cell === null)) {
    setPopMessage("msgPopAllClear", { score: popGame.score });
  }
}

