function setReactionPadState(state, label) {
  reactionGame.padNode.className = `reaction-pad ${state}`;
  reactionGame.padNode.textContent = label;
}

function startReactionGame() {
  window.clearTimeout(reactionGame.timeoutId);
  reactionGame.armed = true;
  reactionGame.ready = false;
  renderReactionHUD();
  setReactionPadState("waiting", t("reactionWaitLabel"));
  setReactionMessage("msgReactionWait");
  const delay = 1200 + Math.random() * 2400;
  reactionGame.timeoutId = window.setTimeout(() => {
    reactionGame.ready = true;
    reactionGame.startTime = performance.now();
    setReactionPadState("ready", t("reactionNowLabel"));
    setReactionMessage("msgReactionNow");
  }, delay);
}

function handleReactionPad() {
  if (!reactionGame.armed) {
    startReactionGame();
    return;
  }

  if (!reactionGame.ready) {
    window.clearTimeout(reactionGame.timeoutId);
    reactionGame.armed = false;
    setReactionPadState("idle", t("reactionEarlyLabel"));
    setReactionMessage("msgReactionEarly");
    return;
  }

  const time = performance.now() - reactionGame.startTime;
  reactionGame.armed = false;
  reactionGame.ready = false;
  reactionGame.best = reactionGame.best === null ? time : Math.min(reactionGame.best, time);
  renderReactionHUD(time);
  setReactionPadState("idle", `${Math.round(time)} ms`);
  setReactionMessage("msgReactionRetry");
}

reactionGame.startButton.addEventListener("click", startReactionGame);
reactionGame.padNode.addEventListener("click", handleReactionPad);

