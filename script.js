function toggleBreakoutRunning() {
  if (breakoutGame.bricks.every((brick) => !brick.alive) || breakoutGame.lives <= 0) {
    resetBreakoutGame();
  }
  breakoutGame.running = !breakoutGame.running;
  breakoutGame.paused = !breakoutGame.running;
  setBreakoutMessage(breakoutGame.running ? "msgBreakoutRunning" : "msgBreakoutPaused");
}

function drawBreakout() {
  const { ctx } = breakoutGame;
  ctx.clearRect(0, 0, breakoutGame.width, breakoutGame.height);

  const bg = ctx.createLinearGradient(0, 0, 0, breakoutGame.height);
  bg.addColorStop(0, "#081728");
  bg.addColorStop(1, "#102743");
  ctx.fillStyle = bg;
  ctx.fillRect(0, 0, breakoutGame.width, breakoutGame.height);

  breakoutGame.bricks.forEach((brick) => {
    if (!brick.alive) {
      return;
    }
    ctx.fillStyle = brick.color;
    ctx.fillRect(brick.x, brick.y, brick.width, brick.height);
    ctx.fillStyle = "rgba(255, 255, 255, 0.18)";
    ctx.fillRect(brick.x + 4, brick.y + 3, brick.width - 8, 4);
  });

  ctx.fillStyle = "#dbe9ff";
  ctx.fillRect(breakoutGame.paddle.x, breakoutGame.paddle.y, breakoutGame.paddle.width, breakoutGame.paddle.height);

  ctx.beginPath();
  ctx.arc(breakoutGame.ball.x, breakoutGame.ball.y, breakoutGame.ball.radius, 0, Math.PI * 2);
  ctx.fillStyle = "#66e5ff";
  ctx.fill();

  ctx.fillStyle = "rgba(255, 255, 255, 0.1)";
  ctx.fillRect(0, 0, breakoutGame.width, 36);
  ctx.fillStyle = "#edf6ff";
  ctx.font = '16px "Segoe UI"';
  ctx.fillText(`${t("breakoutScore")} ${breakoutGame.score}`, 18, 24);
  ctx.fillText(`${t("breakoutLives")} ${breakoutGame.lives}`, 160, 24);
}

function loseBreakoutLife() {
  breakoutGame.lives -= 1;
  renderBreakoutHUD();
  if (breakoutGame.lives <= 0) {
    breakoutGame.running = false;
    setBreakoutMessage("msgBreakoutNoLives");
    return;
  }
  breakoutGame.running = false;
  resetBreakoutPositions();
  setBreakoutMessage("msgBreakoutLifeLost", { lives: breakoutGame.lives });
}

function updateBreakout(deltaTime) {
  if (!breakoutGame.running) {
    return;
  }

  if (breakoutGame.input.left && !breakoutGame.input.right) {
    breakoutGame.paddle.x -= breakoutGame.paddle.speed * deltaTime;
  } else if (breakoutGame.input.right && !breakoutGame.input.left) {
    breakoutGame.paddle.x += breakoutGame.paddle.speed * deltaTime;
  }

  breakoutGame.paddle.x = Math.max(0, Math.min(breakoutGame.width - breakoutGame.paddle.width, breakoutGame.paddle.x));

  breakoutGame.ball.x += breakoutGame.ball.vx * deltaTime;
  breakoutGame.ball.y += breakoutGame.ball.vy * deltaTime;

  if (breakoutGame.ball.x - breakoutGame.ball.radius <= 0 || breakoutGame.ball.x + breakoutGame.ball.radius >= breakoutGame.width) {
    breakoutGame.ball.vx *= -1;
  }
  if (breakoutGame.ball.y - breakoutGame.ball.radius <= 36) {
    breakoutGame.ball.vy *= -1;
  }

  const paddle = breakoutGame.paddle;
  if (
    breakoutGame.ball.y + breakoutGame.ball.radius >= paddle.y &&
    breakoutGame.ball.y - breakoutGame.ball.radius <= paddle.y + paddle.height &&
    breakoutGame.ball.x >= paddle.x &&
    breakoutGame.ball.x <= paddle.x + paddle.width &&
    breakoutGame.ball.vy > 0
  ) {
    const hitRatio = (breakoutGame.ball.x - (paddle.x + paddle.width / 2)) / (paddle.width / 2);
    breakoutGame.ball.vx = hitRatio * 340;
    breakoutGame.ball.vy = -Math.abs(breakoutGame.ball.vy);
  }

  breakoutGame.bricks.forEach((brick) => {
    if (!brick.alive) {
      return;
    }
    const hit =
      breakoutGame.ball.x + breakoutGame.ball.radius > brick.x &&
      breakoutGame.ball.x - breakoutGame.ball.radius < brick.x + brick.width &&
      breakoutGame.ball.y + breakoutGame.ball.radius > brick.y &&
      breakoutGame.ball.y - breakoutGame.ball.radius < brick.y + brick.height;

    if (!hit) {
      return;
    }

    brick.alive = false;
    breakoutGame.ball.vy *= -1;
    breakoutGame.score += 10;
    renderBreakoutHUD();
  });

  if (breakoutGame.ball.y - breakoutGame.ball.radius > breakoutGame.height) {
    loseBreakoutLife();
  }

  if (breakoutGame.bricks.every((brick) => !brick.alive)) {
    breakoutGame.running = false;
    setBreakoutMessage("msgBreakoutClear", { score: breakoutGame.score });
  }
}

function breakoutLoop(time = 0) {
  const deltaTime = Math.min((time - breakoutGame.lastTime) / 1000 || 0, 0.032);
  breakoutGame.lastTime = time;
  updateBreakout(deltaTime);
  drawBreakout();
  breakoutGame.frameId = requestAnimationFrame(breakoutLoop);
}

